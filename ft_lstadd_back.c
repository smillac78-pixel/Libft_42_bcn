#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)//introduce nodo final de lista
{
	t_list	*tmp;

	if (!lst || !new)
		return ;
	if (!*lst)
	{
		*lst = new;
		return ;
	}
	tmp = *lst;
	while (tmp->next)
		tmp = tmp->next;
	tmp->next = new;
}
