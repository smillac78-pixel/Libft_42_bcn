#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
//rellena  los primeros n bytes de memoria con el valor c
{
	unsigned char	*ptr;

	ptr = (unsigned char *)s;
	while (n > 0)
	{
		*ptr = (unsigned char)c;
		ptr++;
		n--;
	}
	return (s);
}
