#include "libft.h"

char	*ft_strrchr(const char *s, int c)
	//devuelve un puntero a la ultima aparicion del caracter c
{
	char	*last;

	last = NULL;
	while (*s)
	{
		if (*s == (char)c)
			last = (char *)s;
		s++;
	}
	if (*s == (char)c)
		last = (char *)s;
	return (last);
}
