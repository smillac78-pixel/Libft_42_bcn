#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
//escribe una cadena en un descriptor de archivo
{
	int	i;

	i = 0;
	if (!s)
		return ;
	while (s[i])
	{
		write (fd, &s[i], 1);
		i++;
	}
}
