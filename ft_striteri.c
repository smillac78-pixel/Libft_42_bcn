#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
//aplica una funcion a cada caracter modificando la cadena original
{
	unsigned int	i;

	if (!s || !f)
		return ;
	i = 0;
	while (s[i])
	{
		f(i, &s[i]);
		i++;
	}
}
